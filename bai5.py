# Neu trong chuoi co dau ' thi dung dau " va nguoc lai
strhello = "Hello"
print(strhello)
print(type(strhello))

# Neu trong chuoi co nhieu dong thi dung ''' hoac """
strhello2 = '''Hello
I am fine, thank you
And you?'''
print(strhello2)
print(type(strhello2))

# \n la de xuong dong
strhello3 = 'Hello\nI am not fine, thanks'
print(strhello3)
print(type(strhello3))

